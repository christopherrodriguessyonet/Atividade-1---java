public class Aluno{
    String nome;
    String matricula;
    double nota;
    double forca;
    int dificuldade;

    public Aluno(String nome, String matricula, double nota, double forca, int dificuldade) {
        this.nome = nome;
        this.matricula = matricula;
        this.nota = nota;
        this.forca = forca;
        this.dificuldade = dificuldade;
    }
}
